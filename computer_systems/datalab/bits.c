int negate(int x) {
  return ~x + 1;
}
/* 
 * isNonNegative - return 1 if x >= 0, return 0 otherwise 
 *   Example: isNonNegative(-1) = 0.  isNonNegative(0) = 1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 3
 */
int isNonNegative(int x) {
  x = x >> 31;
  return !x;
}
/* 
 * isGreater - if x > y  then return 1, else return 0 
 *   Example: isGreater(4,5) = 0, isGreater(5,4) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 24
 *   Rating: 3
 */
int isGreater(int x, int y) {
  int x31 = (x >> 31) & 1;
  int y31 = (y >> 31) & 1;
  int sign = x31 ^ y31; //same sign : 0 / diff sign : 1

  int same_sign = !sign & ((~y + x) >> 31); 
 
  int diff_sign = x31 & !y31;
  
  return !(same_sign | diff_sign);
}
/*
 * ilog2 - return floor(log base 2 of x), where x > 0
 *   Example: ilog2(16) = 4
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 90
 *   Rating: 4
 */
int ilog2(int x) {
  int save16, save8, save4, save2, save1;
  int b16, b8, b4, b2; //recursive한 방법으로 풀이
	save16 = !(!(x >> 16)); //앞쪽 16비트가 0이면 0(case1)/ 숫자이면 1(case2)
	b16 = save16 << 4;
	save8 = !(!(x >> (8 + b16))); //case1일때 뒤쪽 16비트의 앞쪽 8비트를 같은 방법으로 확인 / case2일때 앞쪽 16비트의 앞쪽8비트를 확인 
	b8 = (save8 << 3) + b16; //확인한 8비트가 0이면 0x8 + b16 / 숫자이면 1x8 + b16 
	save4 = !(!(x >> (4 + b8)));
	b4 = (save4 << 2) + b8; 
	save2 = !(!(x >> (2 + b4)));
	b2 = (save2 << 1) + b4;
	save1 = (x >> (1 + b2)); //마지막까지 같은 방식으로 구해주면된다. 
	return save1 + b2; //만약 x = 2^15라하면 b16 = 0, b8 = 8, b4 = (8 + 4), b2 = (8 + 4 + 2), save1= 1이된다. 따라서 b2 + save1 = 15
}
